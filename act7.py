# 기초 비교연산

# 문제1 - 정수 2개 입력받아 비교하기1
a, b = input().split()
print(int(a) < int(b))

# 문제2 - 정수 2개 입력받아 비교하기2
a, b = input().split()
print(int(a) == int(b))

# 문제3 - 정수 2개 입력받아 비교하기3
a, b = input().split()
print(int(a) <= int(b))

# 문제4 - 정수 2개 입력받아 비교하기4
a, b = input().split()
print(int(a) != int(b))